class TotalHeight {
    public static void main(String args[]){
        int feetHeight = 5;
        double heightInch = 2.5;
        double totality = feetHeight * 12 + heightInch;
        System.out.println("Your height (the feet part) is " + feetHeight);
        System.out.println("and the inches part is " + heightInch);
        System.out.println("Your total height is " + totality);
    }
}
